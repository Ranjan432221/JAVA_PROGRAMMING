public class increement {
    public static void main(String args[]){
        int  i = 56;
        System.out.println(i++);//output is 56 its 1st give the value than increment
        System.out.println(i);//output is 57:above value 1st give same value than increment so here output is 57
        System.out.println(++i);//here 1st i is increment then it assign :output=>58
        System.out.println(i);//this only direct assign the above value
        int b = 45;
        System.out.println(--b);//this is give the out put 44 cause 1st it will decrement then assign 
        System.out.println(b--);//this will give also same 44 output
        System.out.println(b);//it will give 43 output cause above value 1st assign then decrement,so next line it give 43
        int y = 7;
        System.out.println(++y*8);//it gives 64 outputj
        char ch = 'a';
        System.out.println(++ch);//it gives 64 output cits the increment of character
    }
}
