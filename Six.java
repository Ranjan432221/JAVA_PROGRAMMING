import java.util.Scanner;

public class Six {
   //for loop using in java
   public static void main(String[]args){
            int i,n;
            System.out.println("enter the range of no");
            Scanner sc = new Scanner(System.in);
            n = sc.nextInt();
            for(i=0;i<n;i++)
            {
                System.out.println(i);
            }
   } 
}
